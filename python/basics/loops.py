#while
i = 1
while i < 10:
     print(i)
     i += 1
print("#############################################")
#for
for i in range(1, 10):
     print(i)
print("#############################################")

#for with else
for i in range(1, 10):
     print(i)
else:
     print("i is not in the range")
print("#############################################")

#for in list
friends = ["John", "Bob", "Mary"]
for friend in friends:
     print(friend)
print("#############################################")

#for in string
for char in "abcdefghijklmnopqrstuvwxyz":
     print(char)
print("#############################################")